package com.java.sprintQaClass04;

public class IfElseExample {
	
	public static void main(String args[]) {
		
		// if a is greater than b then print "A is greater" else print "B is greater"
		int a = 100;
		int b=2022;
		
		if( a > b) {
			System.out.println("A is greater");
			
		}
		else {
			System.out.println("B is greater");
		}
		
		// if age is greater than 18 then print "You can vote" else print "You are not eligible to vote"
	}

}
