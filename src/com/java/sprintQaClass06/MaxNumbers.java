package com.java.sprintQaClass06;

public class MaxNumbers {

	public static void main(String[] args) {
		
		int a = 10, b = 35 , c = 202;
		
		if( a > b && a > c) {
			System.out.println(a + " is max");      
		}
		else if( b > c) {
			System.out.println(b + " is max");
		}else {
			System.out.println(c + " is max");
		}
		
		
		
	}

}
