package com.java.sprintQaClass10;

public class ForLoopBasic {

	public static void main(String[] args) {
		
		//for(initialisation ; condition ; increment/dec){ }
		
		//print all numbers from 1-5
		for(int i =1 ; i<=5 ; i++) {
			System.out.println(i);
			
		}

		//print your name 7 times using for loop
		for(int i =1;i<=7;i++) {
			System.out.println("Konica");
		}
		//print all numbers from 10-1 using a for loop
		for(int i = 10; i>=1 ;i--) {
			System.out.println(i);
		}
		
	}

}
