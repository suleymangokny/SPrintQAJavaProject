package com.java.sprintQaClass02;

public class ComparisonOperator {
	
	public static void main(String args[]) {
		
		int price =108;
		
		System.out.println( price != 100);
		
		System.out.println(2000 > 9);     //greaterThan
		System.out.println( 8 < 4);       //lessThan
		
		int a =10 , b =20;
		
		System.out.println( a == b);  // false   //comparison operator
		
		System.out.println(a = b);    //20       //assignment operator
		
	}

}
